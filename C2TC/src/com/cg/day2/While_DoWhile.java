package com.cg.day2;

public class While_DoWhile {

	public static void main(String[] args) {
		//While
		int i = 1, n = 5;

	    // while loop from 1 to 5
	    while(i <= n) {
	      System.out.println(i);
	      i++;
	    }
	    System.out.println("===========");
	    //do while
	    int s = 1, j = 5;

	    // do...while loop from 1 to 5
	    do {
	      System.out.println(s);
	      s++;
	    } while(s <= j);
	  }

	}

